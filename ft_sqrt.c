int	ft_sqrt(int nb)
{
	int i;
	int value;

	i = 0;
	value = nb / 2;
	if (nb == 1)
		return (1);
	while (i <= value)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
