#include "libft.h"

char	*ft_strnew(size_t n)
{
	char *s;

	if (!(s = (char *)malloc(sizeof(char) * n + 1)))
		return (NULL);
	ft_bzero(s, n);
	s[n] = '\0';
	return (s);
}
